package fr.dsia.logex.model;

public class RequestDto {

	private String date;
	private String info;

	public RequestDto() {
		super();
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getInfo() {
		return info;
	}

	public void setInfo(String info) {
		this.info = info;
	}

}
